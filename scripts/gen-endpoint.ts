// --- Helper functions ---

function buildRouteBody(method: string, apiPath: string): string {
  const hasIdParam = apiPath.includes(":id");

  if (method === "GET" && !hasIdParam) {
    return `const result = await service.${operationName}(req.tenantId, req.query);
      res.json(result);`;
  }
  if (method === "GET" && hasIdParam) {
    return `const result = await service.${operationName}(req.tenantId, req.params.id);
      res.json(result);`;
  }
  if (method === "POST") {
    return `const result = await service.${operationName}(req.tenantId, req.body);
      res.status(201).json(result);`;
  }
  if (method === "PUT" || method === "PATCH") {
    return `const result = await service.${operationName}(req.tenantId, req.params.id, req.body);
      res.json(result);`;
  }
  if (method === "DELETE") {
    return `await service.${operationName}(req.tenantId, req.params.id);
      res.status(204).send();`;
  }
  return `// TODO: Implement route handler`;
}

function buildSchemaSnippet(
  method: string,
  opName: string,
  pascal: string,
): string {
  if (method === "GET" || method === "DELETE") {
    return `// No request body schema needed for ${method}`;
  }
  return `const ${opName}Schema = z.object({
  // TODO: Add request body fields
  // field: z.string().min(1),
});

export type ${toPascalCase(opName)}Input = z.infer<typeof ${opName}Schema>;

// Add to schema export object:
// ${opName}: ${opName}Schema,`;
}

function buildServiceParams(method: string, apiPath: string): string {
  const hasId = apiPath.includes(":id");
  if (method === "GET" && !hasId) return ", params: any";
  if (method === "GET" && hasId) return ", id: string";
  if (method === "POST") return ", dto: any";
  if (method === "PUT" || method === "PATCH") return ", id: string, dto: any";
  if (method === "DELETE") return ", id: string";
  return "";
}

function buildServiceReturn(method: string, pascal: string): string {
  if (method === "DELETE") return "void";
  return pascal;
}

function buildServiceBody(
  method: string,
  apiPath: string,
  pascal: string,
): string {
  if (method === "DELETE") return `await this.repo.delete(tenantId, id);`;
  return `// TODO: Implement\n  throw new Error('Not implemented');`;
}

function describeOperation(method: string, apiPath: string): string {
  const lastSeg = apiPath.split("/").filter(Boolean).pop() || "";
  if (method === "GET") return `retrieve ${lastSeg.replace(/:/, "")}`;
  if (method === "POST") return `create a new record`;
  if (method === "PUT" || method === "PATCH") return `update the record`;
  if (method === "DELETE") return `delete the record`;
  return `handle ${method} ${apiPath}`;
}
