/**
 * Generate a single API endpoint snippet for an existing module.
 * Usage: pnpm gen:endpoint <METHOD> </api/path> <module_name>
 */

import * as path from "node:path";
import { parseArgs, validateArgs, type CliConfig } from "./lib/cli.js";
import {
  toKebabCase,
  toPascalCase,
  toCamelCase,
  toSnakeCase,
} from "./lib/naming.js";
import { writeFile, reportCreatedFiles } from "./lib/writer.js";

const VALID_METHODS = ["GET", "POST", "PUT", "PATCH", "DELETE"] as const;

const config: CliConfig = {
  scriptName: "endpoint",
  description:
    "Generate a single API endpoint snippet to paste into an existing module",
  args: [
    {
      name: "method",
      description: `HTTP method (${VALID_METHODS.join("|")})`,
      required: true,
    },
    {
      name: "path",
      description: "API path (e.g., /api/cone-purchases/:id/approve)",
      required: true,
    },
    { name: "module_name", description: "Target module name", required: true },
  ],
  examples: [
    "pnpm gen:endpoint POST /api/cone-purchases cone-purchase",
    "pnpm gen:endpoint GET /api/wager-returns/:wagerId wager-return",
    "pnpm gen:endpoint PUT /api/damages/:id/approve damage",
  ],
};

const parsed = parseArgs(config);
const [rawMethod, apiPath, rawModule] = validateArgs(parsed, config);

const method = rawMethod.toUpperCase();
if (!VALID_METHODS.includes(method as any)) {
  console.error(
    `Invalid method: ${method}. Must be one of: ${VALID_METHODS.join(", ")}`,
  );
  process.exit(1);
}

const kebab = toKebabCase(rawModule);
const pascal = toPascalCase(rawModule);
const camel = toCamelCase(rawModule);
const snake = toSnakeCase(rawModule);

// Derive operation name from path and method
const pathSegments = apiPath.split("/").filter((s) => s && !s.startsWith(":"));
const lastSegment = pathSegments[pathSegments.length - 1];
const operationName = lastSegment
  ? toCamelCase(`${method.toLowerCase()}_${lastSegment}`)
  : toCamelCase(`${method.toLowerCase()}_${rawModule}`);

const pascalOperation = toPascalCase(operationName);

// Create snippet file
const safePathName = apiPath.replace(/[/:]/g, "-").replace(/^-+|-+$/g, "");
const snippetFile = `${method.toLowerCase()}-${safePathName}.snippet.ts`;
const snippetPath = path.resolve(
  "src/modules",
  kebab,
  "_generated",
  snippetFile,
);

